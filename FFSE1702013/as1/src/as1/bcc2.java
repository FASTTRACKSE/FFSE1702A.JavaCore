package as1;
import java.util.Scanner;
public class bcc2 {
	 public static void main(String[] args) { 
         
	        Scanner s = new Scanner(System.in); 
	        System.out.print("Nhập vào 1 số: "); 
	      
            
               
	        int n = s.nextInt();
	        for (int j =2; j < 10; j++) { 
	        	for ( int i = 1; i<= n ; i ++) {
	            System.out.print(i + " x " + j + " = "  + i * j + "\t"); 
	        } 
	       
	        
	        
	        System.out.println(""); 
}


} }
