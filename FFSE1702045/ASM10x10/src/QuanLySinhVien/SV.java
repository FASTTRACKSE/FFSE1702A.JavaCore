package QuanLySinhVien;

public class SV {
private int id;
private String nameSV;
private int yearsSV;
private String addressSV;
private float avgSV;
public int getId() {
	return id;
}
public void setId(int id) {
	this.id = id;
}
public String getNameSV() {
	return nameSV;
}
public void setNameSV(String nameSV) {
	this.nameSV = nameSV;
}
public int getYearsSV() {
	return yearsSV;
}
public void setYearsSV(int yearsSV) {
	this.yearsSV = yearsSV;
}
public String getAddressSV() {
	return addressSV;
}
public void setAddressSV(String addressSV) {
	this.addressSV = addressSV;
}
public float getAvgSV() {
	return avgSV;
}
public void setAvgSV(float avgSV) {
	this.avgSV = avgSV;
}

}
